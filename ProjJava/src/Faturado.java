public class Faturado {
    public int mes;
    public char epoca;
    public float preco;
    public int quantidade;
    public String cliente;

    public Faturado(int mes, char epoca, float preco, int quantidade, String cliente) {
        this.mes = mes;
        this.epoca = epoca;
        this.preco = preco;
        this.quantidade = quantidade;
        this.cliente = cliente;
    }


}
